import kotlin.math.*


fun main() {
    // Math functions: PI, E, max, min, round, ceil, floor, abs, sin, cos, random, sign, pow, sqrt.

    val pi = PI
    println(pi)

    val e = E
    println(e)

    println(max(2, 3))
    println(min(2,3))

    println(round(2.3))
    println(round(2.8))

    println(ceil(2.1))
    println(ceil(2.9))

    println(floor(2.1))
    println(floor(2.9))

    println(abs(5.5))
    println(abs(-5.5))
    println(abs(5))

    println(sin(PI/2))

    println((1..10).random())

    println(sign(3.0))
    println(sign(-3.0))

    println(2.0.pow(2))

    println(sqrt(16.0))
}