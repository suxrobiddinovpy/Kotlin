fun main() {
    var a = 15
    var b = 6

    // Qo'shish
    var result = a+b
    println("15+6=$result")
    // Ko'paytirish
    result = a*b
    println("15*6=$result")
    // Ayirish
    result = a-b
    println("15-6=$result")
    // Bo'lish
    result = a/b
    println("15:6=$result")
    // Qoldiqli Bo'lish
    result = a%b
    var result1 = 15/6
    println("15%6=$result1 ($result)qoldiq")
}